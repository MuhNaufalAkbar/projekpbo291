    private void nama_barangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nama_barangActionPerformed
String NamaBarang=(String)nama_barang.getSelectedItem();
 switch (NamaBarang){
 case "Beras":
 harga_barang.setText("12000");
 break;
 case "Minyak Goreng":
 harga_barang.setText("15000");
 break;
 case "Telur":
 harga_barang.setText("3000");
 break;
 case "Tepung":
 harga_barang.setText("5000");
 break;
 case "Gula":
 harga_barang.setText("4000");
 break ;    
 }// TODO add your handling code here:
    }//GEN-LAST:event_nama_barangActionPerformed

