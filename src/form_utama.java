 int JumlahHarga=Integer.parseInt(jumlah_harga.getText());
 if (JumlahBayar>=JumlahHarga){
 jumlah_kembalian.setText(Integer.toString(JumlahBayar-JumlahHarga)); 
 } 
 else {
 jumlah_kembalian.setText("Uang Anda Kurang");
 }
